// Operadores Lógicos
const condicaoA = true;
const condicaoB = true;

// Comparação AND eu uso este símbolo &&
console.log(condicaoA && condicaoB);

// Comparação OR eu uso este símbolo ||
console.log(condicaoA || condicaoB);

// Se eu quiser negal algo eu usa a exclamação na frente do boleano
const condicaoC = !true;
console.log(condicaoA && condicaoC);